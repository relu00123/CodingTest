#include <bits/stdc++.h>
#include <unordered_map>
#include <unordered_set>

using namespace std;

int main() {
	ios::sync_with_stdio(0);
	cin.tie(0);


	// 가장 적은 종류의 비행기를 타고 모든 국가 여행
	// n개국을 여행할 것임

	int tc; cin >> tc;
	while (tc--) {
		int n, m; cin >> n >> m;

		for (int i = 0; i < m; ++i) {
			int a, b; cin >> a >> b;  // a와 b를 연결하는 그래프가 있다. 

		}

		cout << n - 1 << "\n";
	
	}


	return 0;
}